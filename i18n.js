function t(key) {
  const lang = localStorage.getItem('kul_lang') || 'en';
  return (I18N[lang] && I18N[lang][key]) || I18N.en[key] || key;
}

function setLang(lang) {
  localStorage.setItem('kul_lang', lang);
  window.location.reload();
}

function currentLocale() {
  const lang = localStorage.getItem('kul_lang') || 'en';
  return (I18N[lang] && I18N[lang].locale) || 'en-IN';
}
